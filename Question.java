public class Question {
    private int id;
    private String text;
    private String pillar;

    public Question (int id, String text, String pillar) {
        this.id = id;
        this.text = text;
        this.pillar = pillar;
    }

    //Getters

    public int getId() {
        return id;
    }

    public String getText() {
        return text;
    }
    public String getPillar() {
        return pillar;
    }





}
